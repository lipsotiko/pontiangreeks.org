      <p style="text-align: center">
        <strong>CHRISTOS ILIADIS</strong>
      </p>
      <p>
        I was born during 1911, in the village Ahurnu, south of Ordu, in the
        Black Sea region. I had nine brothers and sisters. My father&#8217;s
        name was Panagiotis and my mother was named Pelagia.
      </p>
      <p>
        When the tragic moment came, they gathered all the Christians of our
        area. I was only five years old at the time and cannot remember much
        more. All I recall is that only my sister Evropi and I survived; the
        rest of my family perished.
      </p>
      <p>
        Many of the women in our village, followed the Gendarmes that night,
        while all the men were exiled. Through their tears and sobbing, these
        women tried to elicit the sympathy of the gendarmes and the soldiers who
        monitored the deportation.
      </p>
      <p>
        When night came, the cries of children from each home could be heard out
        on the streets. Even the yelping dogs could sense the evil occurring.
      </p>
      <p>
        In our home that night, there were only six children. My four sisters
        and my brother Haralampos who was not older than fifteen. I was only
        five years old. We agonized over the return of our friends, relatives,
        and neighbors. In vain, our poor mother followed the route of exile
        taken by my father and her older sons.
      </p>
      <p>
        All women abandoned their search, and returned home, desperate and dying
        from marching in the cold weather. Our mother also returned alone. One
        hour later, she was dead. Her death left six orphans abandoned and
        unprotected. I was too young at the time to fully understand the loss of
        my mother.
      </p>
      <p>
        Our village was destroyed. No men remained. Everyone had gone on foot to
        the city of Ordu. Without any support, or protection, we also took the
        road to Ordu, which offered greater safety. We embarked on this
        destination on the advice given by our church. Thank God, the people of
        Ordu helped us. They provided us with shelter and food.
      </p>
      <p>
        Soon after, we faced a new calamity. Turkish police arrived and they
        gathered all females over the age of fifteen, kidnapping my three
        sisters. Now only the three of us children remained in Ordu.
      </p>
      <p>
        Much time had passed and we heard no news about the girls. Later, some
        people said that the Turkish policemen sexually abused them all and then
        threw them down the cliff. Only God knows what really happened. Many
        Christian families in Ordu adopted the orphans. My brother was adopted
        by a family called Grigoriadu. My sister was adopted by a family called
        Semertzidis. By now, I had turned 6 years old.
      </p>
      <p>
        I was also adopted by a family but after three days they threw me out on
        the street, leaving me at God&#8217;s mercy, because I was very weak and
        they were afraid I might get sick. All children like me, who were not
        accepted into households with families, were instead placed in an
        orphanage administered by the rich Christians of Ordu. The school
        building next to IPAPANTI Church, became our orphanage
      </p>
      <p>
        One day in 1916, or 1917, I do not remember the exact date, the Russian
        fleet of the Black Sea, appeared in Ordu. The Turkish population of Ordu
        ran away to the mountains because there were great rumors that many
        Armenians were serving within the Russian army vessels. The Russians
        brought boats to the coast and gathered Christians on their fleet to be
        transferred to Russia. The teachers and personnel of our orphanage ran
        out to the sea, leaving behind the crying orphans.
      </p>
      <p>
        We then left our orphanage and ran towards the sea coast, to save
        ourselves by boarding the Russian boats. I was six and a half years old
        and I found myself responsible for my own survival. I managed to enter a
        small boat which exceeded capacity and could not depart from the coast.
        My brother and my sister left with their new parents to Russia.
      </p>
      <p>
        Before I realized it, I was thrown out of the boat. A man threw me out
        and the only thing I could to at that moment was cry.
      </p>
      <p>
        When the Russian fleet left, the Turkish police returned to Ordu. They
        were evicting the residents from their homes. They also came to our
        orphanage to find 100 of us. They were very angry as they forced us
        outside. They informed us of their new plan in which we were to be
        transported away from the coast. We were then exiled &#8211; memories of
        which, can bring shivers to one's spine. From this exile, I only
        remember a two month martyr's march. 2,500 souls departed Ordu and by
        the end of this ordeal, only 1,350 skeletons, resembling human beings,
        remained.
      </p>
      <p>
        Some of us were taken to Erbaa, others to Tokat. We were kept alive with
        a little bread and some olives.
      </p>
      <p>
        One morning, they told us we could return to Ordu. I was very glad to
        hear of this, not expecting to find a deserted city.
      </p>
      <p>
        Our Patriarch took care of our arrival in Constantinople. We all had
        tears in our eyes as we bid a final farewell to our beloved homeland. In
        Constantinople we were brought to an orphanage located on one of the
        islands on the Sea of Marmama. There were 700 orphans from several
        regions. They cut our hair, gave us a bath along with clean clothes and
        shoes.
      </p>
      <p>
        It was here where I took my first school lessons. There were some
        American ladies who also brought us some gifts. It was at that time when
        I received the first gift of my life, a small ball, which I kept like a
        talisman. One day a picnic was also organized for us.
      </p>
      <p>
        We stayed in that orphanage for about four years and were then taken to
        Greece. The orphans were relocated to several orphanages around the
        country. I finally entered the Naval School for officers in the island
        of Poros.
      </p>
      <p>
        When the holidays came, any of my classmates who had a mother or other
        relatives, received permission to leave school and visit their families.
        Those of us had nobody else on earth, had to stay at school. I will
        never in my life forget the feelings of joy, when our classmates
        returned from vacations and brought some gifts to me saying: “Take it.
        This gift is from my mother to you”. My eyes were full of tears knowing
        that there were still somebody in the world who cared about me.
      </p>
      <p style="text-align: left">
        I graduated from the naval school and served on the Greek Naval Fleet
        for thirty seven years. Now, at the end of my life, I am glad to be a
        grandfather and I thank God that everyting came to a good conclusion
      </p>
