      <p>
        All the Pontians in the circle, from the first to the last, dance with
        the same intensity. There is no lead dancer, no protagonist, who
        performs special figures or intricate movements. No one asks the
        musicians to play a special tune so that he may head the dance, as is
        the case in other, more open, individualistic societies. No one dances
        solo here. Everyone participates as an equal. The disciplined group of
        dancers, whether of men, women, or both, moves as one. Its joy is an
        affirmation of that sense of personal sharing in every common
        undertaking. Each Pontian bears the same burden of responsibility, and
        the unanimity of the dancers can be likened to the united efforts of
        members of a ship&#8217;s crew battling against a storm. A wrong step by
        the third dancer or a slight hesitation by the sixth would immediately
        mar the overall effect. The Pontians&#8217; attitude to dance is the
        same as their attitude to everyday life, where the entire village is
        responsible for the community&#8217;s survival. No one follows, and no
        one leads.
      </p>
      <p>
        The dance circle is small. The participants may be many but the group
        does not spread out, it has learned to dance close together. Outdoor
        feasts were few in Pontos, on account of the cold climate and the summer
        rains -not to mention the necessary precautions under Ottoman rule
        &#8211; and most celebrations were held indoors. Even in the
        countryside, the highland meadows, the famous parcharia, or in church
        courtyards, the circle may become denser but not wider……
