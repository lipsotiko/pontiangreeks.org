        <strong>Dr. William Samonides:</strong>
        He came to Pontian studies reluctantly—he was not aware of it and mostly
        not interested. But it took hold when he was working on a family history
        project that went out of control. It started when he talked to the Greek
        community in Canton, Ohio.
      </p>
      <p>
        Largely unexplored was the history of the Pontian Greek people there.
        They were proud of their past but couldn't articulate it—they felt their
        story was important but lost.
      </p>
      <p>
        He gathered Pontian history in the U.S. so it would not be irretrievably
        lost.
      </p>
      <p>
        Gather documents and photos. Videotape and preserve the memories of the
        older generation—the first generation of Pontian Greeks that came
        here—because they know the story of their parents. An important part of
        the Pontian experience is the survival story of the tens of thousands of
        Pontians who immigrated to America. The study of the Pontian diaspora in
        North America is important to the larger story of the Pontians. Their
        unique story is also a universal story. This makes it important because
        of our shared humanity and shared inhumanity.
      </p>
