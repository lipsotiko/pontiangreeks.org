        <strong>Professor Eleni Phufas:</strong>
        Her background is Arcadian, not Pontian, so she came to the topic
        through her Sudanese students. Leni Riefenstahl, biographer of Adolf
        Hitler, lived in the Sudan. Her students talked about the terrible
        genocide and oppression there. She began reading and viewed a film by an
