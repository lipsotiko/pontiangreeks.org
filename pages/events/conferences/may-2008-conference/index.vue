      <p>
        <strong
          >“The Armenian Genocide: A Model for the Study of Persecuted
          Peoples”</strong
        >
      </p>
      <p>
        <em
          >The Zoryan Institute for Armenian genocide studies is a model of how
          a research center specializing in the history of the Greeks of Asia
          Minor and Pontus can be set up. The Pontian Genocide is important in
          the larger field of human rights studies and is therefore relevant
          today.
        </em>
      </p>
      <p>
        <em
          >In the 1970s, a group of Armenians wanted a forum for analytical,
          scholarly, and detached study of their history, identity, and future
          as a nation.
        </em>
      </p>
      <p>
        <em
          >The trauma of the 1915 genocide was so overwhelming for
          Armenians—especially in the diaspora—that they were still in survival
          mode generations later: their actions were not the result of
          strategies.</em
        ><em
          >Armenian intellectuals were relegated only to classroom teaching, and
          their essential role of providing theoretical ideas and the practical
          application of those ideas was disregarded.</em
        >
      </p>
      <p>
        <em
          >The forum would reestablish Armenian intellectuals in a leadership
          role as part of an effort to understand what happened, how it
          happened, and why it happened. What does it mean to be an Armenian
          when your people have not lived in Armenia for centuries? What are the
          markers of Armenian identity, and can you have more than one cultural
          identity?
        </em>
      </p>
      <p>
        <em
          >Armenians needed to understand the diaspora experience and confront
          the issues of assimilation, loss of language, intermarriage, cultural
          preservation, genocide recognition, and the struggle for an
          independent homeland.
        </em>
      </p>
      <p>
        <em>The Zoryan organization was formed with three missions:</em>
      </p>
      <ol>
        <li>
          <em
            >Understand the forces and factors that brought Armenians to where
            they are today;</em
          >
        </li>
        <li>
          <em
            >Express their history in their own voice and define themselves—not
            to leave that definition to others; and</em
          >
        </li>
        <li>
          <em
            >Educate and involve the Armenian community in a higher level of
            discourse without claiming to have all the answers</em
          >
        </li>
      </ol>
      <p>
        <em
          >We want to help our people understand where they came from and how
          they got here (the genocide) and where we are now (the diaspora) so
          they can make informed decisions about their future as individuals and
          as a nation.</em
        >
      </p>
      <p>
        <em>The Zoryan Institute research center:</em>
      </p>
      <ol>
        <li>
          <em>Collects, organizes, and preserves archival documents;</em>
        </li>
        <li>
          <em>Identifies issues vital to the Armenian community;</em>
        </li>
        <li>
          <em
            >Researches and analyzes the issues by working with individuals,
            scholarly teams, and by organizing high level conferences;</em
          >
        </li>
        <li><em>Publishes research findings;</em></li>
        <li><em>Organizes educational programs; and</em></li>
        <li>
          <em
            >Provides for the future of Armenian scholarship, through
            universities, to motivate and train the next generation of
            scholars</em
          >
        </li>
      </ol>
      <p>
        <em>The institute's success has been based on:</em>
      </p>
      <ol>
        <li>
          <em
            >Work that is conducted using the highest standards of
            scholarship;</em
          >
        </li>
        <li>
          <em
            >Our issues being viewed in a universal context, in relation to what
            else was happening in the world at the time;</em
          >
        </li>
        <li>
          <em
            >Our issues studied in comparison with similar experiences of other
            nations, such as the investigation into the diaspora's meaning from
            the experiences of Armenians, Greeks, and Jews; and</em
          >
        </li>
        <li>
          <em
            >Using an interdisciplinary approach, conducting analysis from the
            perspectives of history, political science, sociology, law, and
            other disciplines</em
          >
        </li>
      </ol>
      <p>
        <em
          >These principles attracted to the Institute non-Armenian academics,
          who found common ground between their work and ours. The institute
          successfully attracts dissident scholars, who are driven by an ethical
          motivation to understand their own countries' roles in genocide and
          its denial (e.g., Turkey, Germany, Israel), which in turn helps deepen
          our understanding of the genocide and exposes its ongoing denial.
        </em>
      </p>
      <p>
        <em
          >The benefits of an academic research institute for the Greeks of Asia
          Minor and Pontus include:</em
        >
      </p>
      <ol>
        <li>
          <em
            >Provides a structure to establish the facts and experiences of the
            Greeks of Asia Minor and Pontus;</em
          >
        </li>
        <li>
          <em
            >Identifies and addresses political, social, and educational
            issues;</em
          >
        </li>
        <li>
          <em
            >Eliminates the anger and emotionalism of sensitive issues through a
            scholarly approach that promotes sound decision-making;</em
          >
        </li>
        <li>
          <em>
            Broadens our understanding by analysis and comparison of our issues
            with others and from the perspective of many disciplines;</em
          >
        </li>
        <li>
          <em
            >Establishes a communications program through publications,
            conferences, and public lectures to create awareness of the Pontian
            Greek experience;</em
          >
        </li>
        <li>
          <em>Assists in developing young scholars to enter the field;</em>
        </li>
        <li>
          <em
            >Develops educational publications and programs for the non-academic
            community, both Greek and non-Greek;</em
          >
        </li>
        <li>
          <em
            >Studies contemporary Pontian society in the U.S. and other
            countries and the impact of the genocide on its development and
            identity; and</em
          >
        </li>
        <li>
          <em
            >Prepares compelling reasons and arguments for elected officials in
            the U.S. and other countries to address the political concerns and
            issues of the Pontian Greek community
          </em>
        </li>
      </ol>
      <p>
        <em
          >Genocide—its pain and trauma—is not uniquely Greek or Armenian. It is
          a human experience shared by many and is ongoing today. People see how
          elected officials could intervene to save lives rather than avoid
          involvement. The world's people today are more aware of genocide and
          more receptive to learning about it. They understand it is a gross
          violation of human rights and when one people's rights are trampled,
          their own are not secure. We must all promote awareness to prevent it
          from happening again. Promoting awareness of the Pontian genocide is
          an integral part of this process.
        </em>
      </p>
