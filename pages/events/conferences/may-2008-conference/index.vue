      <p>
        <strong>Mr. Shirinian</strong>: Prof. Stavrou told us history is not a
        fact until someone writes about it. The Zoryan Institute was formed in
        1982 because documentation was important. What are the archival sources
        for Greek history?
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: Memoirs and published histories are
        secondary sources because they have no footnotes, but they constitute
        95% of history. In fact, some have documentation, but sources are not
        cited for Pontos histories, or the footnotes shown are too general.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Any fact cited must be verifiable, or it
        won't be accepted.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: There are excellent archives in Athens, at
        the Center for Asia Minor Studies. The 1908-1922 period has so many
        dimensions, including documents in Russia, personal archives of Greeks
        of the diaspora, and personal libraries of Greeks that are not public
        and unavailable to scholars until they are bequeathed to a public
        institution.
      </p>
      <p>
        There is information—how do you identify and preserve it so it is
        accessible by scholars who can document what is said?
      </p>
      <p>
        <strong>Dr. Samonides</strong>: There are many sources for the early
        lives of Pontian immigrants in the U.S. Since all men registered for the
        draft, lots of information was collected on them, such as date of birth,
        place of birth, where they lived, etc. Other sources include
        naturalization papers, census information, church records, and city
        directories. There are newspaper articles about what was going on in the
        Pontian community. There are ship manifests that tell who the person
        came to visit, their relationship, and who was left behind.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: The U.S. government was involved with
        the Ottoman Empire, so there is a wealth of information: diplomatic
        correspondence for the U.S., Great Britain, France, Germany; Russian and
        Ottoman archives. These are good sources that provide lots of work for
        many generations of scholars.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: Students need to have the languages—it is
        best to know 4 to 5 languages. The archives will be written in 15-20
        languages. There exists a catalog of names and maps of areas to be
        exterminated. This provides intent on the part of the Turks and is part
        of the debate on denying genocide.
      </p>
      <p>
        We need to train people in the languages they need to do the research so
        the story can be told in all its complexity.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Consider the physical nature of the
        documents. Many are handwritten paper documents located in other
        countries. One can take photos of them or scan them and post them on the
        Internet.
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: When you write articles, include as much
        of the original document as possible in the body of the paper. This is
        another way of getting original information to more and more people.
      </p>
      <p>
        <strong>Dr. Samonides</strong>: There is a lifetime of work for
        scholars. When language ability is limited or at the undergraduate
        level, original scholarship can be done using public works, for example.
        Photos are very important—scan them and find information about them
        while people are still available who can do this. For unidentified
        photos, put them on website for a wider audience of people to see and
        provide identification.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Photographic evidence is doubly
        powerful. There are many ways of identifying photos, such as by the
        background curtain used in a certain studio, or other identifying marks.
      </p>
      <p>
        <strong>Dr. Samonides</strong>: Hold photo parties. Bring copies of old
        photos and invite people to identify them.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: Owners often don't want to part with
        photos until it is too late. Children will clean out a collection and
        toss old photos if they don't know the people. Sometimes photo libraries
        are brought in but they may be only partial collections. We need to save
        entire collections—get owners to promise to donate the library but let
        them hold onto them until they are ready to part with them.
      </p>
      <p>
        It is important when preserving material to notify the community and
        make a master catalog of what's available.
      </p>
      <p>
        <strong>Dr. Samonides</strong>: Ask for old 78 rpm records. Make copies
        and keep a collection of Pontian music.
      </p>
      <p>
        <strong>Professor Phufas</strong>: Look to other visuals, not just
        books. There are Pontian Greek and Asia Minor Greek families who have
        websites. Some have Web pages with photos and stories. Link to these
        sites through an online portal. Use technology to reach out.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: As to the location of archival material, we know several countries
          where individual professors have it. It needs to be released so work
          is not duplicated. We need to start to know where material exists and
          ask if the individual will give us the information.</em
        >
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: One reason this is difficult is they are
        still using the material. When a research center is set up, Dr.
        Psomiades will turn over all material, except what he is currently
        working on. Not many scholars have material—about a handful.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Sometimes scholars hold documents and
        won't share them. You need to establish one place to locate them and
        share them.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: We need to credit the person who did the work. Often it is a
          problem to read something and then try to find a document that is
          listed in reference.
        </em>
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: When you have a center doing serious
        work and it is seriously financed, you will be surprised at how much
        comes in. We don't have such an archive anywhere in the world right now,
        though one is being set up in Australia.
      </p>
      <p>
        <strong>Professor Pfufas</strong>: Why can't you digitally archive
        materials so they are transmittable anywhere?
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: Part of the reason is they don't want to
        release documents for duplication.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: We need to know who is working on what archives so work is not done
          twice. Government organizations in Turkey have archives, too.
          Rockefeller Center in New York, the International Red Cross, YMCA,
          missionary works (they wrote with lots of detail). There are
          university archives not yet seen. We also need young scholars to do
          this work.</em
        >
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: YMCA archives are part of social work
        archives at University of Minnesota. Your list is indicative of the
        immensity of the problem. Once you establish a base to house archives,
        you have incentive and pressure to get these documents.
      </p>
      <p>
        But, you can't just bring a load of archives to, say, the University of
        Chicago. They won't take it because space and personnel, etc., are
        limited. It helps if you have one staff person studying in the subject
        area. Librarians are there to preserve materials until the right person
        comes along to use it.
      </p>
      <p>
        First, gather materials so you can approach a university with something
        real.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: We all see the necessity of a center for Pontian and Asia Minor
          archives. Where will the funds come from to gather, store, and
          catalog? What do we need to do? Will the center be standalone or in
          conjunction with an educational institution or another cultural
          institution?</em
        >
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: We're brainstorming the business case.
        You need to overwhelm the community with the importance of this work to
        push this forward.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: This is a first step. The Greek-American Community could support
          us. This is why we are filming our conference—to demonstrate this is a
          serious effort.
        </em>
      </p>
      <p>
        <em
          >We want to emphasize the catastrophe of Asia Minor. We want the Greek
          government to spend money for university chairs on Modern Greek
          History. We hope some institutions out there will respond if the
          request comes from people like you on the panel.</em
        >
      </p>
      <p>
        <strong>Dr. Stavrou</strong>. They think of you as ambassadors, and you
        get compliments on your work. But they realize generations of people
        studied the Greek Pontians out of passion, and now no one is replacing
        them.
      </p>
      <p>
        Greece sends a few million dollars to organizations around the world as
        a symbolic gesture. You need someone in the business or political world
        to get any money—it is very difficult.
      </p>
      <p>
        <strong>Dr. Samonides</strong>: Both Psomiades and Stavrou have
        sacrificed and been honored by students and universities. They devoted
        much of their professional lives to establishing centers, but it is a
        thankless job.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: All the funding for the Zoryan Institute
        since 1982 is from many individuals giving small donations of $100-$250
        each, plus a few large donors. The Greek diaspora will have to step
        up—don't depend on a government.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: The trip has been rough, but there is lots
        of satisfaction. Attract graduate students with fellowships endowed by
        private citizens. There is a lot of goodwill out there from Greeks and
        non-Greeks. Governments will do only so much—and they can create
        complications.
      </p>
      <p>
        You need a mechanism to approach citizens. You need long range planning,
        but you can do many things in the process.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: Look to the Pontian Greek organization in Greece for help—meet with
          their leadership to see how they can help. They know a center has to
          be established outside of Greece.</em
        >
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Taner Akçam is researching the common
        plight of the Armenians and Greeks in the ethnic homogenization policy
        of the Young Turks. Such research has to be disseminated to the public
        and governments, to motivate them that new work is being done and new
        information being uncovered. Solutions to support such work have to be
        proposed. The media can help, including the <em>Hellenic Voice</em> and
        <em>Greek Star</em> papers.
      </p>
      <p>
        The Greek media have space to fill and will print articles, especially
        if you include photos. Follow-up articles are needed. I recommend
        concentrating on written media—and do it over and over again. Write
        stories so people who are not familiar with the Pontian Greek Genocide
        will read about it.
      </p>
      <p>
        The Greek media should have been here. Mr. Shirinian volunteered to be
        part of a speakers bureau. The organization needs a Web site.
      </p>
      <p>
        Research is essential. Scholars have obligations when publishing to make
        sure their work is original. This is a painstaking process that can take
        years, but there is a power to academic research. A publication
        documenting the German role in the Armenian genocide helped lead to its
        recognition in Germany, but it took 8 years of research and preparation.
        What does it take to interest a person to do research?
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: I He went to Marseilles for the
        Constantinides archives, but his nephew, who inherited the archive, died
        just as I arrived. No one knows where the archives went.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: I am looking for an area of research on
        Pontian history, and I can help with promoting the subject through the
        work I do at the University of Minnesota.
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: Why didn't the work of the Zoryan
        Institute go to the university? What is the history behind archives not
        being at a university but at an independent center?
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Most of the scholars are with the
        university, but some aren't. If we attempted to institutionalize the
        archives, they may not have been accepted because the Armenian genocide
        is so politicized. What is the status of the Pontian Genocide?
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: There is ongoing publishing in Greek,
        and several people make a living as Pontian scholars in Greece. Some of
        it has been translated into English. The problem is there is a lot of
        material that is just straight descriptive and not analytical.
      </p>
      <p>
        We have to rely on Greece to do the work because they get paid for it.
        No one can make a living at it here. It is disappointing there are no
        young scholars.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: Most work being done is not analytical. In
        reading Fotiadis' work, he was really impressed by the amount of
        productivity. All this is unknown outside Greece, because Greek scholars
        are not comfortable with English.
      </p>
      <p>
        It is encouraging that a few young scholars are getting degrees in the
        U.S. He would like to see a course in 20th century Europe, with emphasis
        on the Asia Minor disaster.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Zoryan has commissioned research from
        scholars. Discuss subjects that need to be addressed. What is the
        feasibility of a major translation program?
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: Translation is so important because so
        many documents and works of fiction, memoirs, and first-person
        narratives are in Greek. These works are fabulous, emotionally touching,
        and have great interest beyond the Greek community.
      </p>
      <p>
        Translating Asia Minor Greek from the 1920s into English that is
        readable by an American audience requires a special translator.
        Translating academic Greek into English is also a specialty.
      </p>
      <p>
        It will take hundreds of hours of research to figure out what some works
        mean—for example, some Turkish is written using some Greek characters.
        Greek of a particular era is not easily translated into English of
        another era. Making it accessible in another language means not a
        literal translation, but rather must be interpreted translations that
        don't lose the beauty of the original, the essence of the work.
      </p>
      <p>
        Narratives of people who came through the suffering need to be
        translated into English. These are moving memoirs. Why aren't more of
        them translated?
      </p>
      <p>
        Many first-person experiences haven't been written yet. These are
        important for posterity.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: This is the kind of rich literature that,
        if translated, would make a strong impact as antiwar literature.
      </p>
      <p>
        Most Greek literature is not as accessible as it should be. There is a
        project in Greece to publish about 50 works of modern Greek literature
        and to translate them into several languages. Two are now completed.
      </p>
      <p>
        If the entire program of 50 is completed, it will make a good resource
        for teachers.
      </p>
      <p>
        In addition to translations of individual works, an anthology of several
        works of Asia Minor history would get an audience. This will lift Greek
        literature to modern European standards.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: Are there a number of books that cover the Asia Minor catastrophe
          and experience of Pontian Greeks? How do we know which are credible
          and which are not? Who determines which are credible?</em
        >
      </p>
      <p>
        <em
          >Is anyone working on a comprehensive academic work on the history of
          the Pontian Greek Genocide in English?</em
        >
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: Lots of books have chapters, but there
        is no comprehensive study.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em>: What in Greek needs to be translated?</em>
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: The Fotiadis volumes, especially the 15th,
        which needs to be reedited. It requires some introductory pages and some
        work to make it sensible to an English-speaking audience.
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: It needs more than superficial work.
        There also is the problem of putting it together.
      </p>
      <p>
        <strong>Dr. Smonides</strong>: The central issue is how to get scholarly
        works from the native language into English. We must cultivate more from
        scholars because now there is nothing to read.
      </p>
      <p>
        The Japanese put lots of effort and money into translating and making
        literature available to students to become the next generation of
        scholars. They published the works by bringing in professional
        translators but the end result was worthless. The best way is work with
        scholars who select works and translate them to make them accessible to
        English speakers. These were valuable and useful. The responsibility
        falls on the Greek government and Ministry of Education to do this.
      </p>
      <img src="~/assets/images/placeholder_300x200.jpg" />
      <p>
        <strong>Dr. Stavrou</strong>: Don't expect the Greek government to do
        this. The Ministry of Education pays only $2,500 to a translator; no one
        will take the job. Copy editing and distribution is an even bigger
        problem for any book to be worth the investment—figure the printing cost
        times seven. If your book can recover these costs, it is worth printing.
      </p>
      <p>
        Works published by universities have their own credibility, but
        universities won't publish if they're not making money. The information
        must get into hands of an interested audience—both scholars and the
        general public.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: What is the role of the World Consulate of Hellenes abroad? What
          can they do to help translate books and establish archives?</em
        >
      </p>
      <p>
        They are a government agency so they cannot assist if the government is
        unwilling.
      </p>
