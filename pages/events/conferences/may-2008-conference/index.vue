      <p>
        <strong>Dr. Harry Psomiades:</strong>
        We are fortunate that much of the groundwork for the study of genocide
        in Asia Minor has been done by Armenian scholars with whom we share
        common experiences in place and in time. It is their pioneering work,
        particularly since the last four decades, that has paved the way for a
        better understanding of what happened, how it happened and why these
        monstrous acts of inhumanity took place in Anatolia during World War 1
        and its aftermath. Undoubtedly, the study of genocide world wide derives
        its impulse from the horrors of the Jewish Holocaust in Nazi Germany and
        by the United Nations active involvement in genocide prevention. An area
        completely ignored by its predecessor the League of Nations.
      </p>
      <p>
        Moreover, the survivors of the Greek genocide who came to Greece or went
        to the diaspora had been so profoundly traumatized by their experiences
        (the suicide rate in the first year upon their arrival in Greece was
        more than twice the rate of the native population) and so completely
        involved with the task of mere survival that their story was delayed for
        an inordinate period of time. This silence was reinforced by the Greek
        Governments&#8217; desire not to compromise its relationship with its
        Turkish neighbor. Moreover, the international community was in a denial
        mode. Finally, a significant portion of the intellectual elite of Pontic
        Hellenism had been exterminated in the death marches and in the
        Kemalist&#8217; prisons of Amasia.
      </p>
      <p>
        Dr. Psomiades is currently working on a major study about Fridtjof
        Nansen, the famous Norwegian explorer and League of Nations High
        Commissioner, who helped the relief effort for refugees in the Near East
        after World War I.
      </p>
