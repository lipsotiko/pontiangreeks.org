      <p>
        <strong> Dr. Theofanis Stavrou: </strong>
        <em
          >Anna Sikelianos said, “I do not want you to forget—what happened and
          what historians think happened.” History isn't fact until it is
          written about. In the 20th century, the people of Asia Minor were not
          historical fact because the expulsion and exterminations were not
          talked about. Few studies were done on behalf of the Greeks and Asia
          Minor. We can learn from the Armenians that we should not expect Greek
          experiences to be researched by others. Greeks must write the history
          of the Greeks of Asia Minor.
        </em>
      </p>
      <p>
        <em
          >Do history properly, but do it in dialogue with others.
          Contextualizing is important. We can be pessimistic that little has
          been done to understand the resilience and survival of the Armenians
          and Greeks. They need to be studied.
        </em>
      </p>
      <p>
        <em
          >There is a danger in letting others define you—it lets them set the
          terms of history (such as in Turkey's denials). Write it yourself.</em
