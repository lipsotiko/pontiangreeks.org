        months in Asia Minor/Turkey during the 1920s. The essence of the
        translation was not the literal words but bringing into English from the
        original Greek the feelings expressed by the author. Professor Phufas
        wants to use scholarly resources to popularize stories—translate more
        personal memoirs into English.
