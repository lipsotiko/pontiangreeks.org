      <p>The panelists took questions and comments from the floor:</p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: In regard to the 40 years of silence: Why didn't the older
          generation speak of their experiences?</em
        >
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: They were so traumatized, they just
        wanted to forget and go on with their lives. The refugees who came to
        Greece had difficulty adjusting—they were resented by the locals.
      </p>
      <p>
        The pain was so great, but they had to put it aside to adjust to new
        situations. There was guilt among those who survived, while so many
        others did not. Parents did not want to burden their children with
        irresolvable experiences.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: How do we popularize what happened? What do we do in North America
          to get recognition from the U.S. and Canadian governments? How do we
          get it in the minds of non-Pontians? Do we need movies? Articles?</em
        >
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: Literature and fiction are good but
        first you need to establish the facts. Memoirs are not considered
        definitive.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: Lots of people are not convinced. Having
        lots of photos is good. Workshops are needed to share experiences and
        develop practical measures we can take.
      </p>
      <p>
        Our identity and background in the modern world are ideas that are
        promoted through universities—universities provide a forum that has a
        multiplying effect through generations of students. Teaching students
        has a multiplying effect to their families, friends, and associates.
      </p>
      <p>
        Anything helps, but be especially concerned that we integrate
        experiences of these people into the curriculum of the university.
        People coming out should at least be aware.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em>: Do Pontians have a role in creating chairs at universities?</em>
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: This is one objective a small group can
        take to get money, instead of presenting lots of ideas and getting
        nothing done.
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: One concern with a university is the
        proposal must demonstrate that there are appropriate educational
        resource materials—and we are not there yet.
      </p>
      <p>
        <strong>Dr. Samonides</strong>: We also need to demonstrate significant
        interest. Today, schools in Greece teach about classical Greece; little
        is taught about Byzantium, even before the Pontians.
      </p>
      <p>
        In contrast, the Japanese government spends millions on education.
        Warner educated Stimson to glories of Japanese culture. Movies, novels,
        exhibits—yes to all—put a face to suffering. Large groups have power to
        communicate, but it is not same as creating something that can touch
        people directly as photos, movies, and literature can do.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: The Greek government has a problem
        sponsoring this education. The Turkish government, on the other hand,
        spends millions to establish university chairs for the Turkish
        revisionist line. The government should not be invoked to politicize a
        subject.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: How do the Armenians view the radical acts that accompanied the
          protests? What is the effect on the Pontians?</em
        >
      </p>
      <p>
        <strong>Mr. Shirinian</strong>: The Armenians' success came from the
        diaspora community during the '70s and '80s. Because of the exchange
        treaty, the case for recognition of the Pontian case as a genocide is
        diminished. The geopolitics of Greece is its relationship to NATO, and
        this affects the Greek government's actions relating to this history.
      </p>
      <p>
        A scholar can provide the tools for support in appeals to the
        government, but the Greeks don't have strong documentation and a good
        argument now.
      </p>
      <p>
        <strong>Dr. Psomiades</strong>: We must establish this as an area of
        serious study. There is a record of attempts to get the Turks to
        acknowledge genocide, but all of it stopped. There are no official
        trials on the Pontian side. In 1921, it was obvious and clear what
        happened in Pontus because it was reported by allies of Turkey (the
        Frunze diary). Initially, he was against the Greeks, but he ended up
        calling for the atrocities to stop.
      </p>
      <p>
        In spring of 1922, British and American missionaries published what
        happened in Pontos. The Western powers said a commission should be
        formed and then argued about who should pay for it. In the end, the
        League did not go far because the Turks limited inquiries to western
        Anatolia, where there were minor atrocities compared to Pontus. The
        Turks insisted no one go to Pontus.
      </p>
      <p>
        The Commission of Inquiry never took place because the Turks would not
        come to the peace table at Lausanne. The Turks needed to give permission
        for the return of Turks from Greece. If Greece expelled the Turks,
        resettling refugee Greeks could fail.
      </p>
      <p>
        The great powers wanted Turkish cooperation for other reasons, so they
        didn't want to pursue the inquiry. The compulsory exchange (the first in
        the world) legitimized Turkish ethnic cleansing.
      </p>
      <p>
        <strong>Dr. Stavrou</strong>: We can't justify Pontian genocide. Others
        are not willing to accept the Pontian genocide because not enough
        overwhelming scholarly information supports it. The task is the
        preparatory work that will make the case. Greek works need to be
        translated. There are lots of areas to be explored.
      </p>
      <p>
        <strong><em>Question</em></strong
        ><em
          >: I take issue with the fact we don't have information. Genocide
          scholars have recognized the Pontian Genocide. Hasn't the legwork
          already been done?
        </em>
      </p>
