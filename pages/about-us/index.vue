      <p>
        The Pontian Greeks are descendants of the Hellenic people who colonized
        the Pontus — the northeast&nbsp;region of Asia Minor on the Black Sea
        (in modern day Turkey) — in the 8th Century BC. They prospered in
        this&nbsp;area and established trade routes, developed mining
        industries, and produced&nbsp;scholars and
        leaders.&nbsp;&nbsp;The&nbsp;Greek&nbsp;Orthodox civilization of Pontus
        came to an end in 1923 when&nbsp;the Greeks of Asia Minor&nbsp;were
        forcibly removed from their homeland&nbsp;in the aftermath of&nbsp;the
        genocide.
      </p>
      <p>
        The Society in Chicago was founded in November 1977 by several Pontian
        Greeks who immigrated to the United States from Greece and countries of
        the former Soviet Union. They&nbsp;were determined to preserve the rich
        heritage that was passed&nbsp;down to them from their parents and
        grandparents, who were expelled from their homeland on the southern
        region of the Black Sea, known as the Pontus, between 1914 and 1923.
      </p>
      <p>
        Today, the children and grandchildren of the founding members continue
        to support the Society's goals to research and share the history
        of&nbsp; their people, to preserve their unique Greek dialect, and to
        perpetuate the&nbsp;great&nbsp;civilization that developed over 3000
        years in the Pontus.
      </p>
      <p>
        The Society organizes lectures and educational programs, hosts annual
        dinner dances, and participates in public events&nbsp;held by&nbsp;the
        Greek community of the Chicago land area.&nbsp; In recent years, several
        members of the Society have also been active in&nbsp;gathering and
        re-printing historical documentation concerning the Pontian Greek
        genocide.
      </p>
      <p><strong>Society Goals</strong></p>
